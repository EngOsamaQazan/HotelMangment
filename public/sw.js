/*
 * Mafraq Hotel — unified Web Push Service Worker.
 *
 * Responsibilities:
 *   1. Receive Web Push payloads from `src/lib/push/server.ts` and surface
 *      them as OS notifications styled with the hotel's brand (green +
 *      gold calligraphy) — regardless of which module triggered them
 *      (WhatsApp, chat, tasks, reservations, maintenance).
 *   2. Handle quick-reply text input on WhatsApp pushes (Android Chrome
 *      fires `event.reply` with the user-typed text — we forward it to
 *      `/api/whatsapp/push/quick-reply` without opening the PWA).
 *   3. Route notificationclick events to the deep link of the source
 *      module, preferring an already-open tab (instant, no reload).
 *   4. Relay "something new arrived" to open clients so they can play
 *      in-app sounds + flash the tab title.
 *
 * Kept tiny + vanilla JS: it runs in every browser's SW runtime with no
 * bundler. Any change here must be backward-compatible with older subs.
 *
 * Branding assets (generated by `scripts/_gen-brand-icons.cjs`):
 *   - /whatsapp-icon.png     → 512×512 full-colour hotel square
 *   - /whatsapp-badge.png    → 96×96 white-on-transparent status-bar mark
 *   - /icon-512.png          → generic fallback if the brand icon 404s
 */

/* eslint-disable no-restricted-globals */

const BRAND = {
  icon: "/whatsapp-icon.png",
  badge: "/whatsapp-badge.png",
  fallbackIcon: "/icon-512.png",
  hotelName: "فندق المفرق",
};

self.addEventListener("install", (event) => {
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(self.clients.claim());
});

self.addEventListener("push", (event) => {
  let data = {};
  try {
    data = event.data ? event.data.json() : {};
  } catch (_) {
    data = {
      title: BRAND.hotelName,
      body: event.data ? event.data.text() : "",
    };
  }

  const title = data.title || BRAND.hotelName;
  const body = data.body || "";
  const mod = data.module || "whatsapp";

  // Relay `WA_PUSH` to open clients even for non-WA modules; the generic
  // AppShell listens and refreshes its bell. Keep the event name for
  // backward-compat with existing listeners.
  const relay = self.clients
    .matchAll({ type: "window", includeUncontrolled: true })
    .then((clients) => {
      for (const c of clients) {
        try {
          c.postMessage({ type: "WA_PUSH", payload: data });
        } catch (_) {
          /* ignore */
        }
      }
    });

  const options = {
    body,
    icon: data.icon || BRAND.icon,
    badge: data.badge || BRAND.badge,
    // Large inline hero image (Android expanded view, desktop rich push).
    // Only show it if the caller opted in — avoids cluttering simple
    // text-only notifications. Browser fetches same-origin → cookies flow.
    image: data.image || undefined,
    tag: data.tag || `${mod}-notify`,
    renotify: true,
    requireInteraction: data.requireInteraction === true,
    silent: !!data.silent,
    dir: "auto",
    lang: "ar",
    // WhatsApp-like chime vibration (short-short-short-long). Ignored on iOS.
    vibrate: data.silent ? undefined : [120, 60, 120, 60, 200],
    timestamp: Date.now(),
    // Action buttons (incl. quick-reply "text" action on Android Chrome).
    // The payload comes pre-tailored per-module from `src/lib/push/server.ts`.
    actions: Array.isArray(data.actions)
      ? data.actions.map((a) => ({
          action: a.action,
          title: a.title,
          type: a.type || "button",
          placeholder: a.placeholder,
        }))
      : [
          { action: "open", title: "فتح" },
          { action: "dismiss", title: "تجاهل" },
        ],
    data: {
      module: mod,
      url: data.url || "/",
      contactPhone:
        (data.data && data.data.contactPhone) || data.contactPhone || null,
      conversationId:
        (data.data && data.data.conversationId) || data.conversationId || null,
      messageId:
        (data.data && data.data.messageId) || data.messageId || null,
      extra: data.data || null,
      ts: Date.now(),
    },
  };

  event.waitUntil(
    Promise.all([self.registration.showNotification(title, options), relay]),
  );
});

/**
 * Pick the most helpful client tab to handle a notification click:
 *   1. An already-open tab whose pathname matches the notification's deep
 *      link prefix (e.g. /whatsapp → any /whatsapp tab). We focus + post
 *      a message so React can open the exact sub-view without a reload.
 *   2. Any same-origin tab that supports `navigate()` — redirect it.
 *   3. No tabs → open a fresh window.
 */
async function routeNotificationClick(targetUrl, payload) {
  const allClients = await self.clients.matchAll({
    type: "window",
    includeUncontrolled: true,
  });

  // Module → URL-prefix preferred match. Ordered most-specific first so
  // /whatsapp doesn't match /whatsapp-template or similar.
  const prefix = new URL(targetUrl, self.location.origin).pathname;
  const prefixRoot = "/" + (prefix.split("/").filter(Boolean)[0] || "");

  // 1) Same-section tab.
  for (const client of allClients) {
    try {
      const url = new URL(client.url);
      if (url.pathname === prefix || url.pathname.startsWith(prefixRoot)) {
        await client.focus();
        client.postMessage({
          type: "WA_OPEN_CONVERSATION",
          url: targetUrl,
          contactPhone: payload && payload.contactPhone,
          module: payload && payload.module,
        });
        return;
      }
    } catch (_) {
      /* ignore */
    }
  }

  // 2) Any same-origin tab → navigate it.
  for (const client of allClients) {
    if ("navigate" in client) {
      try {
        await client.focus();
        await client.navigate(targetUrl);
        return;
      } catch (_) {
        /* ignore and try next */
      }
    }
  }

  // 3) No tabs → open a fresh window.
  if (self.clients.openWindow) {
    await self.clients.openWindow(targetUrl);
  }
}

/**
 * Handle the inline quick-reply from a WhatsApp push notification.
 * Android Chrome exposes `event.reply` when the user submits the text
 * input. We POST to the server without opening the PWA, then surface a
 * small "تم الإرسال" confirmation toast.
 */
async function handleQuickReply(event, replyText) {
  const notif = event.notification;
  const contactPhone = notif.data && notif.data.contactPhone;
  const conversationId = notif.data && notif.data.conversationId;

  try {
    if (!contactPhone) throw new Error("no contactPhone on notification");
    const res = await fetch("/api/whatsapp/push/quick-reply", {
      method: "POST",
      credentials: "include",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        to: contactPhone,
        conversationId: conversationId || undefined,
        text: replyText,
      }),
    });
    if (!res.ok) throw new Error(`http ${res.status}`);

    await self.registration.showNotification(`${BRAND.hotelName} — تم الإرسال`, {
      body: replyText.length > 80 ? replyText.slice(0, 80) + "…" : replyText,
      icon: BRAND.icon,
      badge: BRAND.badge,
      tag: `wa-sent-${contactPhone}`,
      silent: true,
      data: {
        module: "whatsapp",
        url: notif.data.url || "/whatsapp",
        contactPhone,
      },
    });
  } catch (err) {
    // Surface a failure toast so the user doesn't think the message went through.
    await self.registration.showNotification(
      `${BRAND.hotelName} — تعذّر إرسال الردّ`,
      {
        body: "افتح المحادثة لإعادة المحاولة",
        icon: BRAND.icon,
        badge: BRAND.badge,
        tag: `wa-sent-err-${contactPhone || "unknown"}`,
        requireInteraction: true,
        data: {
          module: "whatsapp",
          url: notif.data.url || "/whatsapp",
          contactPhone,
        },
      },
    );
  }
}

self.addEventListener("notificationclick", (event) => {
  const notif = event.notification;
  const action = event.action || "";
  const replyText = (event.reply || "").trim();

  // Quick-reply first — don't close the notification on failure, so the user
  // can retry if the network blipped.
  if (action === "reply" && replyText) {
    event.waitUntil(handleQuickReply(event, replyText));
    notif.close();
    return;
  }

  // Dismiss action → just close the toast, no navigation.
  if (action === "dismiss") {
    notif.close();
    return;
  }

  // Default / "open" action → deep-link into the app.
  notif.close();
  const targetUrl = (notif.data && notif.data.url) || "/";
  event.waitUntil(routeNotificationClick(targetUrl, notif.data || {}));
});
